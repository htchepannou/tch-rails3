package com.tchepannou.rails.core.exception;

/**
 * This exception is thrown when an error occurs during the invocation of a method
 * 
 * @author herve
 */
public class MethodInvocationException
    extends RuntimeException
{

    public MethodInvocationException (Throwable cause)
    {
        super(cause);
    }

    public MethodInvocationException (String message, Throwable cause)
    {
        super (message, cause);
    }

    public MethodInvocationException (String message)
    {
        super (message);
    }

    public MethodInvocationException ()
    {
    }

}
